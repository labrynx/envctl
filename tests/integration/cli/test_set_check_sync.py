def test_status_failure_explains_ambiguous_vault_identity(
    runner: CliRunner,
    workspace: Path,
) -> None:
    runner.invoke(app, ["config", "init"], catch_exceptions=False)

    vault_projects_dir = workspace.parent / "home" / ".envctl" / "vault" / "projects"
    first = vault_projects_dir / "demo-a--prj_1111111111111111"
    second = vault_projects_dir / "demo-b--prj_2222222222222222"
    first.mkdir(parents=True, exist_ok=True)
    second.mkdir(parents=True, exist_ok=True)
    payload = (
        '{"version":2,"project_slug":"demo","project_key":"demo","project_id":"%s",'
        '"repo_root":"%s","git_remote":"git@github.com:labrynx/envctl.git",'
        '"known_paths":[],"created_at":"2026-03-30T00:00:00Z","last_seen_at":"2026-03-30T00:00:00Z"}'
    )
    (first / "state.json").write_text(
        payload % ("prj_1111111111111111", str(workspace)),
        encoding="utf-8",
    )
    (second / "state.json").write_text(
        payload % ("prj_2222222222222222", str(workspace)),
        encoding="utf-8",
    )

    result = runner.invoke(app, ["status"], catch_exceptions=False)

    assert result.exit_code == 1
    assert "Error: Ambiguous vault identity for this repository." in result.output
    assert f"repo_root: {workspace}" in result.output
    assert "matching_ids: prj_1111111111111111, prj_2222222222222222" in result.output
    assert "Run `envctl project bind`" in result.output


def test_check_fails_for_invalid_string_json_format(
    runner: CliRunner,
    workspace: Path,
) -> None:
    runner.invoke(app, ["config", "init"], catch_exceptions=False)
    runner.invoke(app, ["init", "--contract", "starter"], catch_exceptions=False)

    schema_path = workspace / ".envctl.schema.yaml"
    schema = yaml.safe_load(schema_path.read_text(encoding="utf-8"))
    schema["variables"]["TEST_JSON"] = {
        "type": "string",
        "format": "json",
        "required": True,
        "sensitive": False,
    }
    schema_path.write_text(yaml.safe_dump(schema, sort_keys=False), encoding="utf-8")

    runner.invoke(app, ["set", "APP_NAME", "demo"], catch_exceptions=False)
    runner.invoke(app, ["set", "PORT", "3000"], catch_exceptions=False)
    runner.invoke(app, ["set", "DATABASE_URL", "https://db.example.com"], catch_exceptions=False)
    runner.invoke(app, ["set", "TEST_JSON", '{\\"broken\\"}'], catch_exceptions=False)

    check = runner.invoke(app, ["check"], catch_exceptions=False)
    assert check.exit_code == 1
    assert "Invalid keys" in check.stdout
    assert "TEST_JSON: Expected a valid JSON string" in check.stdout


def test_add_format_json_persists_and_is_used_by_check(
    runner: CliRunner,
    workspace: Path,
) -> None:
    runner.invoke(app, ["config", "init"], catch_exceptions=False)
    runner.invoke(app, ["init", "--contract", "starter"], catch_exceptions=False)

    runner.invoke(app, ["set", "APP_NAME", "demo"], catch_exceptions=False)
    runner.invoke(app, ["set", "PORT", "3000"], catch_exceptions=False)
    runner.invoke(app, ["set", "DATABASE_URL", "https://db.example.com"], catch_exceptions=False)
    runner.invoke(
        app,
        ["add", "TEST_JSON", '{"ok": true}', "--type", "string", "--format", "json"],
        catch_exceptions=False,
    )

    schema = yaml.safe_load((workspace / ".envctl.schema.yaml").read_text(encoding="utf-8"))
    assert schema["variables"]["TEST_JSON"]["format"] == "json"

    runner.invoke(app, ["set", "TEST_JSON", '{\\"broken\\"}'], catch_exceptions=False)
    check = runner.invoke(app, ["check"], catch_exceptions=False)
    assert check.exit_code == 1
    assert "TEST_JSON: Expected a valid JSON string" in check.stdout


def test_grouped_check_ignores_unrelated_missing_keys(
    runner: CliRunner,
    workspace: Path,
) -> None:
    runner.invoke(app, ["config", "init"], catch_exceptions=False)
    runner.invoke(app, ["init", "--contract", "starter"], catch_exceptions=False)

    schema_path = workspace / ".envctl.schema.yaml"
    schema = yaml.safe_load(schema_path.read_text(encoding="utf-8"))
    schema["variables"]["APP_NAME"]["group"] = "Application"
    schema["variables"]["PORT"]["group"] = "Runtime"
    schema["variables"]["DATABASE_URL"]["group"] = "Database"
    schema["variables"]["APP_URL"] = {
        "type": "string",
        "required": True,
        "sensitive": False,
        "group": "Application",
        "default": "http://${APP_NAME}:${PORT}",
    }
    schema_path.write_text(yaml.safe_dump(schema, sort_keys=False), encoding="utf-8")

    runner.invoke(app, ["set", "APP_NAME", "demo"], catch_exceptions=False)

    result = runner.invoke(
        app,
        ["--group", "Application", "check"],
        catch_exceptions=False,
    )

    assert result.exit_code == 0
    assert "scope: group=Application" in result.stdout
    assert "Environment contract satisfied" in result.stdout


