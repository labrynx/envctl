def test_run_sync_body_matches_dotenv_export_except_for_header(
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    context = make_project_context()
    report = make_resolution_report(
        values={
            "APP_NAME": make_resolved_value(key="APP_NAME", value="demo", source="profile"),
            "PORT": make_resolved_value(key="PORT", value="3000", source="default"),
        }
    )
    captured: dict[str, object] = {}
    contract = make_contract(
        {
            "APP_NAME": make_variable_spec(name="APP_NAME"),
            "PORT": make_variable_spec(name="PORT"),
        }
    )

    monkeypatch.setattr(sync_service, "load_project_context", lambda: (object(), context))
    monkeypatch.setattr(
        sync_service,
        "resolve_projectable_environment",
        lambda _context, *, active_profile, group, operation: (contract, report),
    )
    monkeypatch.setattr(
        sync_service,
        "write_text_atomic",
        lambda path, text: captured.update({"path": path, "text": text}),
    )

    sync_service.run_sync("staging")

    sync_text = captured["text"]
    assert isinstance(sync_text, str)
    assert sync_text.startswith("# Generated by envctl\n")
    assert sync_text.removeprefix("# Generated by envctl\n\n") == "APP_NAME=demo\nPORT=3000\n"


def test_run_sync_rejects_invalid_resolution(
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    context = make_project_context()
    filtered_report = make_resolution_report(missing_required=("DATABASE_URL",))

    monkeypatch.setattr(sync_service, "load_project_context", lambda: (object(), context))
    monkeypatch.setattr(
        sync_service,
        "resolve_projectable_environment",
        lambda _context, *, active_profile, group, operation: (
            raise_projection_error(
                operation=operation,
                profile=active_profile,
                group=group,
                report=filtered_report,
                suggested_actions=("envctl fill", "envctl set KEY VALUE"),
            )
        ),
    )

    with pytest.raises(ValidationError, match="Cannot sync because") as exc_info:
        sync_service.run_sync("dev")

    assert exc_info.value.diagnostics == ProjectionValidationDiagnostics(
        operation="sync",
        active_profile="dev",
        selected_group=None,
        report=filtered_report,
        suggested_actions=("envctl fill", "envctl set KEY VALUE"),
    )


