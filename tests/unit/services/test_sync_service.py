from __future__ import annotations

from pathlib import Path

import pytest

import envctl.services.sync_service as sync_service
from envctl.errors import ValidationError
from envctl.services.projection_validation import ProjectionValidationDiagnostics
from tests.support.builders import make_resolution_report, make_resolved_value
from tests.support.contexts import make_project_context
from tests.support.contracts import make_contract, make_variable_spec
from tests.support.projection_validation import raise_projection_error


def test_run_sync_writes_generated_repo_env_file(
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    context = make_project_context(repo_env_path="/tmp/demo/.env.local")
    report = make_resolution_report(
        values={
            "APP_NAME": make_resolved_value(key="APP_NAME", value="demo", source="profile"),
            "DATABASE_URL": make_resolved_value(
                key="DATABASE_URL",
                value="https://db.example.com",
                source="profile",
                masked=True,
            ),
        },
    )
    captured: dict[str, object] = {}
    contract = make_contract(
        {
            "APP_NAME": make_variable_spec(name="APP_NAME"),
            "DATABASE_URL": make_variable_spec(name="DATABASE_URL"),
        }
    )

    monkeypatch.setattr(sync_service, "load_project_context", lambda: (object(), context))
    monkeypatch.setattr(
        sync_service,
        "resolve_projectable_environment",
        lambda _context, *, active_profile, group, operation: (contract, report),
    )
    monkeypatch.setattr(
        sync_service,
        "render_dotenv",
        lambda values, include_header=False, variable_groups=None: (
            "# Generated by envctl\nAPP_NAME=demo\nDATABASE_URL=https://db.example.com\n"
            if include_header
            else "APP_NAME=demo\nDATABASE_URL=https://db.example.com\n"
        ),
    )
    monkeypatch.setattr(
        sync_service,
        "write_text_atomic",
        lambda path, text: captured.update({"path": path, "text": text}),
    )

    _context, active_profile, target_path = sync_service.run_sync("staging")

    assert active_profile == "staging"
    assert target_path == context.repo_root / ".env.staging"
    assert captured["path"] == context.repo_root / ".env.staging"
    assert (
        captured["text"]
        == "# Generated by envctl\nAPP_NAME=demo\nDATABASE_URL=https://db.example.com\n"
    )


